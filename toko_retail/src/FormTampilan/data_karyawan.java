    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        String jk;
        if(rdlk.isSelected()){
           jk = rdlk.getText();
        }else{
           jk = rdpr.getText();
        }
    String sts;
        if(rdmkh.isSelected()){
           sts = rdmkh.getText();
        }else{
           sts = rdbmkh.getText();
        }
        int ok = JOptionPane.showConfirmDialog(null,"Anda Yakin Ingin Mengedit Data Ini='"+txnama.getText()+"'",".:: Konfirmasi ::.",JOptionPane.YES_NO_OPTION);
        if (ok==0){
        try {
            Connection connection = koneksi_krywn.getKoneksi();
            Statement statement = connection.createStatement();
            String sql="update input_data_karyawan set nip ='"+txnip.getText()+
                        "',nama='"+txnama.getText()+"',jabatan='"+cbjabatan.getSelectedItem()+"',jenis_kelamin='"+jk+"',agama='"+cbagama.getSelectedItem()+"',status='"+sts+"',alamat='"+txalamat.getText()+"'where nip = '"+tabel+"'";
            statement.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "berhasil diubah");
        } catch (SQLException |HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }tampil_karyawan();
    }//GEN-LAST:event_btn_updateActionPerformed

