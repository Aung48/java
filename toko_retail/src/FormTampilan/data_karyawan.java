    private void AmbilAgama(String agama){
        String[]ag = {"Islam","Kristen","Hindu","Budha"};
        cbagama.removeAllItems();
        cbagama.addItem(agama);
    for (String ag1 : ag) {
        if (!ag1.equals(agama)) {
            cbagama.addItem(ag1);
        }
    }
}
    
