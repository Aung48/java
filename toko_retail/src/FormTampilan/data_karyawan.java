    private void tabel_karyawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_karyawanMouseClicked
        // TODO add your handling code here:
        int row = tabel_karyawan.getSelectedRow();
        tabel = model.getValueAt(row, 0).toString();
        txnip.setText(model.getValueAt(row, 0).toString());
        txnama.setText(model.getValueAt(row, 1).toString());
        cbjabatan.setSelectedItem(model.getValueAt(row, 2).toString());
        String jk = model.getValueAt(row, 3).toString();
        AmbilJk(jk);
        cbagama.setSelectedItem(model.getValueAt(row, 4).toString());
        txalamat.setText(model.getValueAt(row, 6).toString());
        String status = model.getValueAt(row, 3).toString();
        AmbilStatus(status);
    }//GEN-LAST:event_tabel_karyawanMouseClicked

