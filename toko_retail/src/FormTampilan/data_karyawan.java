    private void tampil_karyawan(){
        Object[]baris = {"nip", "nama","jabatan","jenis kelamin","agama","status", "alamat"};
        model = new DefaultTableModel(baris,0);
        tabel_karyawan.setModel(model);
        String sql = "select * from input_data_karyawan order by nip desc";
        Connection con;
        Statement st;
        try {
            con = koneksi_krywn.getKoneksi();
            st = con.createStatement();
            ResultSet hasil = st.executeQuery(sql);
            while (hasil.next()){
                String nip = hasil.getString("nip");
                String nama = hasil.getString("nama");
                String jabatan = hasil.getString("jabatan");
                String jenis_kelamin = hasil.getString("jenis_kelamin");
                String agama = hasil.getString("agama");
                String status = hasil.getString("status");
                String alamat = hasil.getString("alamat");
                String[]data = {nip, nama,jabatan,jenis_kelamin,agama,status, alamat};
                model.addRow(data);    
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "menampilkan data gagal", "informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
